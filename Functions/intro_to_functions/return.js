function sum(num1, num2) { // declare a function with 2 parameters: num1 and num2.
    let result = num1 + num2; // add num1 and num2, then store the sum in the result variable
    return result; // return the result. 
}

let total = sum(3, 5); // we're calling the sum function that we declared above and passing in 2 arguments -> 3 (num1) and 5 (num2). Then we're storing the answer in a variable called total.
console.log(total) // 8.


