let num = 0; // this is the counter. After every iteration of the loop, the value of num will increase by 1. 
 
while (num < 5) { // the loop will run as long as this condition is true. 
   console.log("Google"); // During every iteration of the loop, log "google" to the console
   num++; // after you log "google" to the console, add 1 to num above. //                                  
}





